const palette = new function() {
    this.colors = [];
    for (let i = 0; i < 32; i++) {
        this.colors[i] = new Uint8ClampedArray(4);
        this.colors[i][0] = RED[i];
        this.colors[i][1] = GREEN[i];
        this.colors[i][2] = BLUE[i];
        this.colors[i][3] = 0xff;
    }
}
